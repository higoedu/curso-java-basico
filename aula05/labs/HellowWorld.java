package aula05.labs;
/*
nome da classe é o memo no do arquivo.java
HellowWorld é camel case, pois tem mais de uma palavra junta

- declaração da classe - class
- nome da classe - HellowWorld
- nome do método - main
- modificadores de acesso do método - public static
- tipo de retorno do método - void (método não retorna nada)
- parâmetros ou argumentos do método - (String[] args)
- comando que faz output na tela - System.out.println
- blocos, cada parte do programa {}, são delimitados entre chaves

Java é case sensitive, por exemplo se p de public for maiúsculo, não vai funcionar
*/
public class HellowWorld {
    public static void main(String[] args) {
        System.out.println("Hellow World!");
    }
}
